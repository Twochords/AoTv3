SELECT DISTINCT nt.*
FROM spawn2 s
JOIN spawngroup sg ON sg.id = s.spawngroupID
JOIN spawnentry se ON se.spawngroupID = sg.id
JOIN npc_types nt ON nt.id = se.npcID
WHERE s.zone IN ('chambersa','chambersb','chambersc','chambersd','chamberse','chambersf')
  AND s.version IN (0,1,2)
  AND nt.level > 1
  AND nt.merchant_id = 0
  AND nt.bodytype NOT IN (11,33)
  AND LOWER(nt.name) NOT LIKE '%death_touch%'
  AND LOWER(nt.name) NOT LIKE 'george%'
  AND LOWER(nt.name) NOT LIKE 'shell_of_%'
